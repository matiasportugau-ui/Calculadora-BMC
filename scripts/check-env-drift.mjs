#!/usr/bin/env node
/**
 * check-env-drift.mjs — fail when `process.env.X` references in server/ are not
 * documented in either:
 *   - .github/workflows/deploy-calc-api.yml `env_vars:` / `--set-secrets` blocks
 *   - .env.example
 *   - .github/ALLOWED_ENV_DRIFT.txt (explicit allowlist for vars set in Cloud Run console)
 *
 * Exit codes: 0 = no drift, 1 = drift found, 2 = script error.
 *
 * Usage:
 *   node scripts/check-env-drift.mjs           # human report
 *   node scripts/check-env-drift.mjs --json    # JSON report (for CI summary)
 *   node scripts/check-env-drift.mjs --bootstrap > .github/ALLOWED_ENV_DRIFT.txt
 *     (writes the current drift set to stdout so it can seed the allowlist)
 */

import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __filename = fileURLToPath(import.meta.url);
const REPO_ROOT = path.resolve(path.dirname(__filename), "..");
const SERVER_DIR = path.join(REPO_ROOT, "server");
const DEPLOY_YAML = path.join(REPO_ROOT, ".github/workflows/deploy-calc-api.yml");
const ENV_EXAMPLE = path.join(REPO_ROOT, ".env.example");
const ALLOWED_FILE = path.join(REPO_ROOT, ".github/ALLOWED_ENV_DRIFT.txt");

const args = new Set(process.argv.slice(2));
const asJson = args.has("--json");
const bootstrap = args.has("--bootstrap");

function walk(dir, acc = []) {
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    if (entry.name === "node_modules" || entry.name.startsWith(".")) continue;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, acc);
    else if (/\.(m?js|cjs|ts)$/.test(entry.name)) acc.push(full);
  }
  return acc;
}

function scanReferenced() {
  const referenced = new Set();
  const re = /process\.env\.([A-Z][A-Z0-9_]+)/g;
  for (const file of walk(SERVER_DIR)) {
    const txt = fs.readFileSync(file, "utf8");
    let m;
    while ((m = re.exec(txt)) !== null) referenced.add(m[1]);
  }
  return referenced;
}

function scanProvidedFromYaml() {
  const provided = new Set();
  if (!fs.existsSync(DEPLOY_YAML)) return provided;
  const txt = fs.readFileSync(DEPLOY_YAML, "utf8");
  // env_vars: |
  //   KEY=${{ secrets.X }}
  const envBlock = txt.match(/env_vars:\s*\|\s*\n([\s\S]*?)(?:\n\s{0,8}flags:|\n\s{0,8}\w+:)/);
  if (envBlock) {
    for (const line of envBlock[1].split("\n")) {
      const m = line.trim().match(/^([A-Z][A-Z0-9_]+)=/);
      if (m) provided.add(m[1]);
    }
  }
  // --set-secrets=/path=secret-name:version  OR  --set-secrets=KEY=secret-name:version
  for (const m of txt.matchAll(/--set-secrets=([^\s]+)/g)) {
    const left = m[1].split("=")[0];
    const key = left.replace(/^.*\//, "");
    if (/^[A-Z][A-Z0-9_]+$/.test(key)) provided.add(key);
  }
  return provided;
}

function scanDocumented() {
  const documented = new Set();
  if (!fs.existsSync(ENV_EXAMPLE)) return documented;
  for (const line of fs.readFileSync(ENV_EXAMPLE, "utf8").split("\n")) {
    // Accept both active (`VAR=value`) and commented (`# VAR=value`) forms —
    // documenting an optional var with a default is valid documentation.
    const m = line.match(/^\s*#?\s*([A-Z][A-Z0-9_]+)\s*=/);
    if (m) documented.add(m[1]);
  }
  return documented;
}

function scanAllowlist() {
  const allowed = new Set();
  if (!fs.existsSync(ALLOWED_FILE)) return allowed;
  for (const raw of fs.readFileSync(ALLOWED_FILE, "utf8").split("\n")) {
    const line = raw.replace(/#.*$/, "").trim();
    if (!line) continue;
    if (/^[A-Z][A-Z0-9_]+$/.test(line)) allowed.add(line);
  }
  return allowed;
}

function main() {
  const referenced = scanReferenced();
  const provided = scanProvidedFromYaml();
  const documented = scanDocumented();
  const allowed = scanAllowlist();

  const covered = new Set([...provided, ...documented, ...allowed]);
  const drift = [...referenced].filter((v) => !covered.has(v)).sort();

  if (bootstrap) {
    process.stdout.write(
      "# Auto-generated by `node scripts/check-env-drift.mjs --bootstrap`.\n" +
        "# Each entry is an env var referenced in server/ that is set\n" +
        "# manually in the Cloud Run console (not in deploy-calc-api.yml or .env.example).\n" +
        "# Migrate these to the workflow over time, then delete the entry from this file.\n" +
        drift.map((v) => v).join("\n") +
        (drift.length ? "\n" : ""),
    );
    process.exit(0);
  }

  const report = {
    referenced: referenced.size,
    provided: provided.size,
    documented: documented.size,
    allowed: allowed.size,
    drift,
  };

  if (asJson) {
    console.log(JSON.stringify(report, null, 2));
  } else {
    console.log(`Referenced in server/:        ${referenced.size}`);
    console.log(`Provided by deploy-calc-api:  ${provided.size}`);
    console.log(`Documented in .env.example:   ${documented.size}`);
    console.log(`Allow-listed in ALLOWED_*:    ${allowed.size}`);
    console.log("");
    if (drift.length === 0) {
      console.log("✅ No env drift.");
    } else {
      console.log(`❌ Drift: ${drift.length} env var(s) referenced but not documented.`);
      console.log("");
      console.log("Each of the following is read by server/ but not declared in");
      console.log("`.github/workflows/deploy-calc-api.yml`, `.env.example`, or");
      console.log("`.github/ALLOWED_ENV_DRIFT.txt`. Either add it to one of those,");
      console.log("or remove the `process.env.X` reference from the server code.");
      console.log("");
      for (const v of drift) console.log(`  - ${v}`);
    }
  }

  process.exit(drift.length === 0 ? 0 : 1);
}

try {
  main();
} catch (err) {
  console.error("check-env-drift error:", err?.message || err);
  process.exit(2);
}
