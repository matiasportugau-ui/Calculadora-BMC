#!/usr/bin/env node
/**
 * Genera src/data/calculatorDataVersion.js con un hash de los archivos de modelos
 * de la calculadora. Cada vez que se modifican constants.js o matrizPreciosMapping.js,
 * al correr npm run dev o npm run build la versión se actualiza.
 *
 * Uso: node scripts/update-calculator-data-version.js
 * Se invoca desde prebuild y al inicio de dev (package.json).
 */
import crypto from "node:crypto";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, "..");

const DATA_FILES = [
  path.join(ROOT, "src/data/constants.js"),
  path.join(ROOT, "src/data/matrizPreciosMapping.js"),
];

function readSafe(p) {
  try {
    return fs.readFileSync(p, "utf8");
  } catch {
    return "";
  }
}

const combined = DATA_FILES.map(readSafe).join("\n");
const hash = crypto.createHash("sha256").update(combined).digest("hex").slice(0, 10);
const date = new Date().toISOString();

const outPath = path.join(ROOT, "src/data/calculatorDataVersion.js");
const content = `// Generated by scripts/update-calculator-data-version.js — do not edit by hand
// Se regenera al ejecutar npm run version:data, npm run dev o npm run build

export const CALCULATOR_DATA_VERSION = "${hash}";
export const CALCULATOR_DATA_VERSION_DATE = "${date}";
`;

fs.writeFileSync(outPath, content, "utf8");
console.log(`[version:data] CALCULATOR_DATA_VERSION=${hash} -> ${path.relative(ROOT, outPath)}`);
